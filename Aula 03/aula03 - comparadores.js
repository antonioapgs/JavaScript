// // REFERENCIAS
// https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Operators

const a = 10;
const b = '10';
const c = 10;

// // EXEMPLOS

// == (o valor dos operandos é igual?)
console.log('a e b são iguais em valor: ', a == b);
console.log('a e c são iguais em valor: ', a == c);
console.log('b e c são iguais em valor: ', b == c);

// === (o valor e o tipo dos operandos são igual?)
console.log('a e b são iguais em valor e tipo: ', a === b);
console.log('a e c são iguais em valor e tipo: ', a === c);
console.log('b e c são iguais em valor e tipo: ', b === c);

// != (o valor é diferente de y?)
console.log('a e b são diferentes em valor: ', a != b);
console.log('a e c são diferentes em valor: ', a != c);
console.log('b e c são diferentes em valor: ', b != c);

// === (o valor e o tipo são diferentes de y?)
console.log('a e b são diferentes em valor e tipo: ', a !== b);
console.log('a e c são diferentes em valor e tipo: ', a !== c);
console.log('b e c são diferentes em valor e tipo: ', b !== c);

// == (o valor do primeiro operando é maior que o do segundo?)
console.log('a é maior que b: ', a > b);
console.log('a é maior que c: ', a > c);
console.log('b é maior que c: ', b > c);

// == (o valor do primeiro operando é menor que o do segundo?)
console.log('a é menor que b: ', a < b);
console.log('a é menor que c: ', a < c);
console.log('b é menor que c: ', b < c);

// == (o valor do primeiro operando é maior ou igual que o do segundo?)
console.log('a é maior ou igual a b: ', a >= b);
console.log('a é maior ou igual a c: ', a >= c);
console.log('b é maior ou igual a c: ', b >= c);

// == (o valor do primeiro operando é menor ou igual que o do segundo?)
console.log('a é menor ou igual a b: ', a <= b);
console.log('a é menor ou igual a c: ', a <= c);
console.log('b é menor ou igual a c: ', b <= c);